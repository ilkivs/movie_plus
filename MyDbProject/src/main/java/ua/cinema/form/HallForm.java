package ua.cinema.form;

public class HallForm {
	private int id;
	private String number;
	private String is_3D;
	private String is_4DX;
	
	


	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getIs_3D() {
		return is_3D;
	}

	public void setIs_3D(String is_3d) {
		is_3D = is_3d;
	}

	public String getIs_4DX() {
		return is_4DX;
	}

	public void setIs_4DX(String is_4dx) {
		is_4DX = is_4dx;
	}




}
